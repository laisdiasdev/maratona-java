package estruturasCondicionais;

public class ElseIF {

	public static void main(String[] args) {
		
		int nota1 = 5;
		int nota2 = 9;
		double media = (nota1 + nota2)/2;
		
		if (media >= 7) {
			System.out.println("Aprovado!");
		}else {
			System.out.println("Reprovado");
		}
		

	}

}
